package com.as.entity;

public class Examination {
	private Integer e_id;
	private String e_comment;
	private Integer is_overtime;
	private Integer record_id;
	public Integer getE_id() {
		return e_id;
	}
	public void setE_id(Integer e_id) {
		this.e_id = e_id;
	}
	public String getE_comment() {
		return e_comment;
	}
	public void setE_comment(String e_comment) {
		this.e_comment = e_comment;
	}
	public Integer getIs_overtime() {
		return is_overtime;
	}
	public void setIs_overtime(Integer is_overtime) {
		this.is_overtime = is_overtime;
	}
	public Integer getRecord_id() {
		return record_id;
	}
	public void setRecord_id(Integer record_id) {
		this.record_id = record_id;
	}
	

}
